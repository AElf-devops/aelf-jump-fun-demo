import BigNumber from "bignumber.js";
import { ONE, ZERO } from "./constants";

export const SYMBOL_FORMAT_MAP: Record<string, string> = {
  "SGR-1": "SGR",
};

export const formatSymbol = (symbol = "") => {
  if (SYMBOL_FORMAT_MAP[symbol]) return SYMBOL_FORMAT_MAP[symbol];
  return symbol;
};

export const getTVSymbolName = (pathSymbol = "") => {
  const arr = pathSymbol.split("_");
  return arr.map((item) => formatSymbol(item)).join("/");
};

export function timesDecimals(a?: BigNumber.Value, decimals?: string | number) {
  if (!a) return ZERO;
  const bigA = BigNumber.isBigNumber(a) ? a : new BigNumber(a || "");
  if (bigA.isNaN()) return ZERO;
  if (typeof decimals === "string" && decimals.length > 10) {
    return bigA.times(decimals);
  }
  return bigA.times(`1e${decimals ?? 18}`);
}

export const getPriceScale = (price: BigNumber.Value, max = 12) => {
  const bigNum = new BigNumber(price);
  if (bigNum.gte(10)) return 2;
  if (bigNum.gte(1e-4)) return 4;
  const { e } = bigNum;
  if (!e) return 4;
  const d = ONE.plus(e).abs().toNumber() + 4;
  if (d > max) return 12;
  return d;
};

